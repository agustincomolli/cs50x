document.addEventListener('DOMContentLoaded', Initialize);

/**
 * Initializes the webpage by adding a paragraph with a greeting message to the body.
 *
 * @return {void} This function does not return anything.
 */
function Initialize() {
    document.write("<h1>Título agregado con JavaScript</h1>");
    document.write("<p>Bienvenido a la programación Web 😁</p>");
}
